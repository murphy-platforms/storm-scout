const csvPath = path.join(__dirname, 'nws_review_package.csv');
const outPath = path.join(__dirname, 'nws_review_package_with_coords.csv');

const lines = fs.readFileSync(csvPath, 'utf8').split('\n');
const header = lines[0].trimEnd();
const newHeader = header + ',latitude,longitude';

let matched = 0;
let missing = [];

const outputLines = [newHeader];

for (let i = 1; i < lines.length; i++) {
  const line = lines[i].trimEnd();
  if (!line) continue;

  // Extract zip from the 3rd column (index 2)
  // Handle quoted fields
  const fields = [];
  let current = '';
  let inQuotes = false;
  for (const ch of line) {
    if (ch === '"') { inQuotes = !inQuotes; }
    else if (ch === ',' && !inQuotes) { fields.push(current); current = ''; }
    else { current += ch; }
  }
  fields.push(current);

  const zip = fields[2] ? fields[2].trim() : '';
  const coords = COORDS[zip];

  if (coords) {
    outputLines.push(`${line},${coords[0]},${coords[1]}`);
    matched++;
  } else {
    outputLines.push(`${line},,`);
    missing.push(zip);
  }
}

fs.writeFileSync(outPath, outputLines.join('\n') + '\n');

console.log(`Enriched ${matched} rows with coordinates.`);
if (missing.length > 0) {
  console.warn(`Missing coordinates for ${missing.length} zip(s): ${missing.join(', ')}`);
}
console.log(`Output: ${outPath}`);
